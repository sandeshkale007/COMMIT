	RTC_AlarmTypeDef sAlarm;
	RTC_TimeTypeDef sTime;
	RTC_DateTypeDef sDate;
	__HAL_RTC_ALARM_CLEAR_FLAG(&hrtc, RTC_FLAG_WUTF);
	HAL_RTC_GetAlarm(hrtc, &sAlarm, RTC_ALARM_A, FORMAT_BIN);
